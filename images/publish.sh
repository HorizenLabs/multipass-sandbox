#!/usr/bin/env bash
set -euo pipefail

# images/publish.sh — Publish a built image to Backblaze B2 and update manifest
#
# Usage:
#   ./images/publish.sh <image-name> <version> <image-file>
#   ./images/publish.sh base 1.0.0 images/artifacts/mps-base-amd64.qcow2.img
#
# Requires: b2 CLI (authenticated), jq
#
# Environment:
#   MPS_B2_BUCKET         — B2 bucket name (default: from config/defaults.env)
#   MPS_B2_BUCKET_PREFIX  — Path prefix in bucket (default: mps)

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
MPS_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"

# Load defaults
# shellcheck source=../config/defaults.env
source "${MPS_ROOT}/config/defaults.env"

IMAGE_NAME="${1:?Usage: publish.sh <image-name> <version> <image-file>}"
VERSION="${2:?Usage: publish.sh <image-name> <version> <image-file>}"
IMAGE_FILE="${3:?Usage: publish.sh <image-name> <version> <image-file>}"

BUCKET="${MPS_B2_BUCKET:-mps-images}"
PREFIX="${MPS_B2_BUCKET_PREFIX:-mps}"
MANIFEST_FILE="${SCRIPT_DIR}/manifest.json"

# Validate SemVer format
if [[ ! "$VERSION" =~ ^[0-9]+\.[0-9]+\.[0-9]+$ ]]; then
    echo "ERROR: Version must be SemVer format (e.g., 1.0.0). Got: $VERSION"
    exit 1
fi

if [[ ! -f "$IMAGE_FILE" ]]; then
    echo "ERROR: Image file not found: $IMAGE_FILE"
    exit 1
fi

# Detect architecture (prefer TARGET_ARCH env var for cross-built images)
if [[ -n "${TARGET_ARCH:-}" ]]; then
    ARCH="$TARGET_ARCH"
else
    ARCH="$(dpkg --print-architecture 2>/dev/null || uname -m)"
    case "$ARCH" in
        x86_64) ARCH="amd64" ;;
        aarch64) ARCH="arm64" ;;
    esac
fi

echo "=== Publishing ${IMAGE_NAME}:${VERSION} (${ARCH}) ==="

# Read SHA256 from .sha256 sidecar (generated by Packer build)
SHA256_FILE="${IMAGE_FILE}.sha256"
if [[ ! -f "$SHA256_FILE" ]]; then
    echo "ERROR: SHA256 sidecar not found: ${SHA256_FILE}"
    echo "Expected alongside image file (generated by 'make image-*')"
    exit 1
fi
SHA256="$(cut -d' ' -f1 < "$SHA256_FILE")"
echo "SHA256: ${SHA256} (from ${SHA256_FILE})"

# Upload to B2
B2_PATH="${PREFIX}/${IMAGE_NAME}/${VERSION}/${ARCH}.img"
echo "Uploading to b2://${BUCKET}/${B2_PATH}..."
b2 file upload --no-progress "${BUCKET}" "$IMAGE_FILE" "$B2_PATH"

echo "Upload complete."

# Update manifest
echo "Updating manifest..."

if [[ ! -f "$MANIFEST_FILE" ]]; then
    echo "ERROR: Manifest not found at ${MANIFEST_FILE}"
    exit 1
fi

# Extract x-mps metadata from the top-most layer YAML for this flavor
LAYER_MAP="base:base protocol-dev:protocol-dev smart-contract-dev:smart-contract-dev smart-contract-audit:smart-contract-audit"
LAYER_FILE=""
for entry in $LAYER_MAP; do
    lname="${entry%%:*}"
    lfile="${entry#*:}"
    if [[ "$lname" == "$IMAGE_NAME" ]]; then
        LAYER_FILE="${SCRIPT_DIR}/layers/${lfile}.yaml"
        break
    fi
done

META_DISK_SIZE=""
META_MIN_PROFILE=""
META_MIN_DISK=""
META_MIN_MEMORY=""
META_MIN_CPUS=""
if [[ -n "$LAYER_FILE" && -f "$LAYER_FILE" ]] && command -v yq &>/dev/null; then
    META_DISK_SIZE="$(yq '.x-mps.disk_size // ""' "$LAYER_FILE")"
    META_MIN_PROFILE="$(yq '.x-mps.min_profile // ""' "$LAYER_FILE")"
    META_MIN_DISK="$(yq '.x-mps.min_disk // ""' "$LAYER_FILE")"
    META_MIN_MEMORY="$(yq '.x-mps.min_memory // ""' "$LAYER_FILE")"
    META_MIN_CPUS="$(yq '.x-mps.min_cpus // ""' "$LAYER_FILE")"
fi

# Add version entry, update latest pointer, and inject x-mps metadata
RELATIVE_URL="${IMAGE_NAME}/${VERSION}/${ARCH}.img"
TMP_MANIFEST="$(mktemp)"

jq \
    --arg name "$IMAGE_NAME" \
    --arg ver "$VERSION" \
    --arg arch "$ARCH" \
    --arg url "$RELATIVE_URL" \
    --arg sha "$SHA256" \
    --arg disk_size "$META_DISK_SIZE" \
    --arg min_profile "$META_MIN_PROFILE" \
    --arg min_disk "$META_MIN_DISK" \
    --arg min_memory "$META_MIN_MEMORY" \
    --arg min_cpus "$META_MIN_CPUS" \
    '
    # Ensure image entry exists
    .images[$name] //= {"description": "", "versions": {}, "latest": null} |
    # Inject x-mps metadata (overwrite if present)
    (if $disk_size != "" then .images[$name].disk_size = $disk_size else . end) |
    (if $min_profile != "" then .images[$name].min_profile = $min_profile else . end) |
    (if $min_disk != "" then .images[$name].min_disk = $min_disk else . end) |
    (if $min_memory != "" then .images[$name].min_memory = $min_memory else . end) |
    (if $min_cpus != "" then .images[$name].min_cpus = ($min_cpus | tonumber) else . end) |
    # Ensure version entry exists
    .images[$name].versions[$ver] //= {} |
    # Set arch-specific data
    .images[$name].versions[$ver][$arch] = {
        "url": $url,
        "sha256": $sha
    } |
    # Update latest pointer
    .images[$name].latest = $ver
    ' "$MANIFEST_FILE" > "$TMP_MANIFEST"

mv "$TMP_MANIFEST" "$MANIFEST_FILE"
echo "Manifest updated: ${IMAGE_NAME}:${VERSION} (${ARCH})"

# Upload updated manifest to B2
echo "Uploading manifest to b2://${BUCKET}/${PREFIX}/manifest.json..."
b2 file upload --no-progress "${BUCKET}" "$MANIFEST_FILE" "${PREFIX}/manifest.json"

echo ""
echo "=== Published successfully ==="
echo "  Image:    ${IMAGE_NAME}:${VERSION} (${ARCH})"
echo "  B2 path:  b2://${BUCKET}/${B2_PATH}"
echo "  SHA256:   ${SHA256}"
echo "  Manifest: b2://${BUCKET}/${PREFIX}/manifest.json"
